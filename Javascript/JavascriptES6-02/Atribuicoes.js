//Operadores de atribuição funcionam de formas abreviadas: 
//sempre antes do operador de igualdade 
let a = 5
let b = 8
let resultado

resultado = a += b  //a = a + b
console.log(resultado)

resultado = a -= b  //a = a - b
console.log(resultado)

resultado = a *= b  //a = a * b
console.log(resultado)

resultado = a /= b  //a = a / b
console.log(resultado)

resultado = a %= b  //a = a % b
console.log(resultado)

resultado = a **= b //a = a ** b
console.log(resultado)


